    private static Integer outputForPermutation(List<Integer> integers) {
        Integer lastOutput = 0;
        for (int i = 0; i < 5; i++) {
            IOStrategy io = new IterableInputStrategy(Arrays.asList(integers.get(i), lastOutput).iterator());
            Opcode opcode = Opcode.buildFromString(INPUT, io).execute();
            lastOutput = opcode.flushOutput();
        }
        return lastOutput;
    }


